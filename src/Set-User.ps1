function Set-User {
<#
.SYNOPSIS
Sets or creates a User entry in Appclusive.


.DESCRIPTION
Sets or creates a User entry in Appclusive.

By updating a User entry you can specify, if you want to update the Mail address, the description or both.


.OUTPUTS
default


.EXAMPLE
Set-User -Name ArbitraryName -Mail arbitrary@example.com -ExternalId ArbitraryExternalId -ExternalType ArbitraryExternalType

